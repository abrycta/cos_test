/**
 * Class that defines the messaging system requirement
 */
public class MessagingFeature {
    /**
     * Allows a user to send a one to one message to another user
     * @param message contents of message to be sent
     * @return contents of message
     */
    public static String send(String message) {
        return message;
    }

    /**
     * Allows a user to receive a one to one message from another user
     * @param message contents of the message received
     * @return contents of message
     */
    public static String receive(String message) {
        return message;
    }


}
